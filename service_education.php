// Generate an array of the last six months in 'YYYY-MM' format
$months = [];
for ($i = 5; $i >= 0; $i--) {
    $months[] = date('Y-m', strtotime("-$i months"));
}
// Prepare the SQL query
$sql = "
    SELECT 
        region,
        DATE_FORMAT(reporting_period, '%Y-%m') AS month,
        COUNT(*) AS total_clients,
        SUM(CASE WHEN service_against_will = 'No' THEN 1 ELSE 0 END) AS satisfied_clients
    FROM satisfaction
    WHERE reporting_period >= DATE_FORMAT(DATE_SUB(CURDATE(), INTERVAL 5 MONTH), '%Y-%m-01')
    GROUP BY region, month
    ORDER BY region, month
";

$result = mysqli_query($mysqli, $sql) or die(mysqli_error($mysqli));

// Process the results into an associative array
$data = [];
while ($row = mysqli_fetch_assoc($result)) {
    $region = $row['region'];
    $month = $row['month'];
    $percentage = ($row['total_clients'] > 0) ? round(($row['satisfied_clients'] / $row['total_clients']) * 100, 2) : 0;
    $data[$region][$month] = $percentage;
}
