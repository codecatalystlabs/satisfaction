<script type="text/javascript">
     document.addEventListener('DOMContentLoaded', function () {
    fetch('word_cloud.php')
      .then(response => {
        if (!response.ok) {
          throw new Error('Network response was not ok');
        }
        return response.json();
      })
      .then(data => {
        // Convert the data object to an array of [word, frequency] pairs
        const wordEntries = Object.entries(data);

        // Sort the entries by frequency in descending order
        wordEntries.sort((a, b) => b[1] - a[1]);

        // Limit to the top 100 words to optimize performance
        const topWords = wordEntries.slice(0, 100);

        // Normalize frequencies to a range between 10 and 50
        const maxFrequency = topWords[0][1];
        const minFrequency = topWords[topWords.length - 1][1];
        const frequencyRange = maxFrequency - minFrequency || 1; // Prevent division by zero

        const normalizedWords = topWords.map(([word, freq]) => {
          const normalizedFreq = ((freq - minFrequency) / frequencyRange) * 40 + 10;
          return [word, normalizedFreq];
        });

        const canvas = document.getElementById('wordCloudCanvas');

        // Check if the canvas element exists
        if (!canvas) {
          console.error('Canvas element with id "wordCloudCanvas" not found.');
          return;
        }

        // Generate the word cloud
        WordCloud(canvas, {
          list: normalizedWords,
          gridSize: Math.round(16 * canvas.width / 1024),
          weightFactor: 1,
          fontFamily: 'Times, serif',
          color: 'random-dark',
          rotateRatio: 0.5,
          backgroundColor: '#f0f0f0'
        });
      })
      .catch(error => {
        console.error('Error loading word cloud data:', error);
      });
  });

</script>
